-- Sample ascents (recent activity for the dashboard)
-- Spread across the last 30 days to populate analytics.
-- ============================================================

-- We'll insert ascents for the climber users + setters trying each other's routes
-- Using a DO block to generate UUIDs and reference random routes

DO $$
DECLARE
    route_ids UUID[];
    r_id UUID;
    climber_ids UUID[] := ARRAY[
        'c0000000-0000-4000-8000-000000000004'::UUID,
        'c0000000-0000-4000-8000-000000000005'::UUID,
        'c0000000-0000-4000-8000-000000000001'::UUID,
        'c0000000-0000-4000-8000-000000000002'::UUID,
        'c0000000-0000-4000-8000-000000000003'::UUID
    ];
    c_id UUID;
    i INT;
    ascent_types TEXT[] := ARRAY['send', 'flash', 'attempt', 'send', 'flash', 'send'];
BEGIN
    -- Get all active route IDs
    SELECT array_agg(id) INTO route_ids
    FROM routes
    WHERE location_id = 'b0000000-0000-4000-8000-000000000001'
      AND status = 'active';

    -- Generate ~200 ascents spread across the last 30 days
    FOR i IN 1..200 LOOP
        r_id := route_ids[1 + floor(random() * array_length(route_ids, 1))::int];
        c_id := climber_ids[1 + floor(random() * 5)::int];

        INSERT INTO ascents (user_id, route_id, ascent_type, attempts, climbed_at)
        VALUES (
            c_id,
            r_id,
            ascent_types[1 + floor(random() * 6)::int]::ascent_type,
            1 + floor(random() * 4)::int,
            NOW() - (random() * 30)::int * interval '1 day' - (random() * 12)::int * interval '1 hour'
        );
    END LOOP;
END $$;

-- ============================================================
-- Sample ratings
-- ============================================================

DO $$
DECLARE
    route_ids UUID[];
    r_id UUID;
    c_id UUID;
    i INT;
BEGIN
    SELECT array_agg(id) INTO route_ids
    FROM routes
    WHERE location_id = 'b0000000-0000-4000-8000-000000000001'
      AND status = 'active';

    -- Each climber rates ~10 routes
    FOR c_id IN SELECT unnest(ARRAY[
        'c0000000-0000-4000-8000-000000000004'::UUID,
        'c0000000-0000-4000-8000-000000000005'::UUID
    ]) LOOP
        FOR i IN 1..15 LOOP
            r_id := route_ids[1 + floor(random() * array_length(route_ids, 1))::int];
            INSERT INTO route_ratings (user_id, route_id, rating)
            VALUES (c_id, r_id, 2 + floor(random() * 4)::int)
            ON CONFLICT (user_id, route_id) DO NOTHING;
        END LOOP;
    END LOOP;

    -- Update denormalized avg_rating and rating_count
    UPDATE routes r SET
        avg_rating = sub.avg_r,
        rating_count = sub.cnt
    FROM (
        SELECT route_id, AVG(rating)::numeric(3,2) as avg_r, COUNT(*) as cnt
        FROM route_ratings
        GROUP BY route_id
    ) sub
    WHERE r.id = sub.route_id;

    -- Update denormalized ascent_count and attempt_count
    UPDATE routes r SET
        ascent_count = sub.sends,
        attempt_count = sub.total
    FROM (
        SELECT route_id,
            COUNT(*) FILTER (WHERE ascent_type IN ('send', 'flash')) as sends,
            COUNT(*) as total
        FROM ascents
        GROUP BY route_id
    ) sub
    WHERE r.id = sub.route_id;
END $$;
