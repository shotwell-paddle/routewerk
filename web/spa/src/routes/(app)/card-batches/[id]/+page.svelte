        <p class="meta-line">
          <span class="status-pill status-{batch.status}">{batch.status}</span>
          <span class="muted">
            · {batch.route_ids.length} card{batch.route_ids.length === 1 ? '' : 's'}
            · {batch.page_count} sheet{batch.page_count === 1 ? '' : 's'}
