        </div>
      {/if}
    </header>

    {#if batch.status === 'failed'}
      <div class="alert-error">
        <div class="alert-error-body">
          <strong>Last render failed.</strong>
          {#if batch.error_message}<span class="muted">{batch.error_message}</span>{/if}
        </div>
        <button class="alert-action" disabled={retrying} onclick={handleRetry}>
          {retrying ? 'Retrying…' : 'Retry'}
        </button>
      </div>
      {#if retryError}<p class="error">{retryError}</p>{/if}
    {/if}

    {#if missingCount > 0 && resolvedCount > 0}
      <div class="alert-warn">
        <strong>
          {missingCount} of {batch.route_ids.length} routes are no longer available
        </strong>
        — they were deleted, archived, or moved to another location. The PDF
        will render only the {resolvedCount} remaining card{resolvedCount === 1 ? '' : 's'}.
      </div>
    {/if}

    <section class="meta-card">
      <dl class="meta-grid">
        <div><dt>Theme</dt><dd>{themeLabel(batch.theme)}</dd></div>
        <div><dt>Cutter</dt><dd>{cutterLabel(batch.cutter_profile)}</dd></div>
        <div><dt>Status</dt><dd>{batch.status}</dd></div>
      </dl>
      {#if batch.status !== 'failed' && resolvedCount > 0 && !previewBroken}
        <figure class="preview">
          <img
            src={cardBatchPreviewUrl(batch.id)}
            alt="Preview of the first card in this batch"
            loading="lazy"
            width="240"
            onerror={() => (previewBroken = true)} />
          <figcaption class="muted small">First card preview</figcaption>
        </figure>
      {/if}
    </section>

    <section class="card">
      <div class="routes-head">
        <h2>Routes ({editing ? editSelected.size : batch.route_ids.length})</h2>
        {#if !editing}
          <button class="ghost" onclick={openEdit}>Edit routes</button>
        {/if}
      </div>
      {#if editing}
        <p class="muted small">
          Toggle routes to add or remove. Save to update the batch — the next
          download re-renders against the new selection.
        </p>
        <ul class="route-list editable">
          {#each Array.from(routes.values()).sort((a, b) => a.grade.localeCompare(b.grade)) as r (r.id)}
            <li>
              <label class="route-row-edit">
                <input
                  type="checkbox"
                  checked={editSelected.has(r.id)}
                  onchange={() => toggleEdit(r.id)} />
                <span class="color-chip" style="background:{r.color}"></span>
                <span class="grade">{r.grade}</span>
                {#if r.name}<span class="rname">{r.name}</span>{/if}
              </label>
            </li>
          {/each}
        </ul>
        {#if editError}<p class="error">{editError}</p>{/if}
        <div class="edit-actions">
          <button class="primary" disabled={editSaving} onclick={saveEdit}>
            {editSaving ? 'Saving…' : 'Save routes'}
          </button>
          <button disabled={editSaving} onclick={() => (editing = false)}>
            Cancel
          </button>
        </div>
      {:else}
        <ul class="route-list">
          {#each batch.route_ids as rid}
            {@const r = routes.get(rid)}
            <li>
              {#if r}
                <span class="color-chip" style="background:{r.color}"></span>
                <span class="grade">{r.grade}</span>
                {#if r.name}<span class="rname">{r.name}</span>{/if}
              {:else}
                <span class="color-chip placeholder"></span>
                <span class="muted">Route {rid.slice(0, 8)}…</span>
              {/if}
            </li>
          {/each}
        </ul>
      {/if}
    </section>

    <section class="card danger-zone">
      <h2>Danger zone</h2>
      <p class="muted">
        Deleting a batch frees the storage used by its rendered PDF. You
        can always create a new batch with the same route list.
      </p>
      <button class="danger" disabled={deleting} onclick={handleDelete}>
        {deleting ? 'Deleting…' : 'Delete batch'}
      </button>
    </section>
  {/if}
</div>

