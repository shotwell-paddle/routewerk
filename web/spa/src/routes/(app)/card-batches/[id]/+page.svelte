            · created {fmtDate(batch.created_at)}
          </span>
        </p>
      </div>
      {#if batch.status !== 'failed'}
        <div class="header-actions">
          <a class="btn-primary" href={cardBatchDownloadUrl(locId, batch.id)} target="_blank" rel="noreferrer">
            Download PDF
          </a>
          <a
            class="btn-secondary"
