<script lang="ts">
  import { page } from '$app/state';
  import { goto } from '$app/navigation';
  import {
    getCardBatch,
    deleteCardBatch,
    updateCardBatch,
    retryCardBatch,
    listRoutes,
    cardBatchDownloadUrl,
    cardBatchCutlinesUrl,
    cardBatchPreviewUrl,
    CUTTER_PROFILES,
    ApiClientError,
    type CardBatchShape,
    type RouteShape,
  } from '$lib/api/client';
  import { effectiveLocationId } from '$lib/stores/location.svelte';

  let batch = $state<CardBatchShape | null>(null);
  let routes = $state<Map<string, RouteShape>>(new Map());
  let loading = $state(true);
  let error = $state<string | null>(null);
  let deleting = $state(false);

  // Edit-mode state. Pre-populates the selection from the current batch
  // when the user clicks Edit; saving PATCHes the route_ids.
  let editing = $state(false);
  let editSelected = $state<Set<string>>(new Set());
  let editSaving = $state(false);
  let editError = $state<string | null>(null);

  // Retry-state for the failure banner. Posts to .../retry which clears
  // the storage_key + resets status to pending. The next Download
  // re-renders against the same route list.
  let retrying = $state(false);
  let retryError = $state<string | null>(null);

  // First-card preview is a server-rendered PNG. The endpoint may 404
  // if every route in the batch was deleted; hide the figure on error.
  let previewBroken = $state(false);

  function openEdit() {
    if (!batch) return;
    editSelected = new Set(batch.route_ids);
    editError = null;
    editing = true;
  }

  function toggleEdit(id: string) {
    if (editSelected.has(id)) editSelected.delete(id);
    else editSelected.add(id);
    editSelected = new Set(editSelected);
  }

  async function saveEdit() {
    if (!locId || !batchId || editSaving) return;
    if (editSelected.size === 0) {
      editError = 'Pick at least one route.';
      return;
    }
    editSaving = true;
    editError = null;
    try {
      const updated = await updateCardBatch(locId, batchId, Array.from(editSelected));
      batch = updated;
      editing = false;
    } catch (err) {
      editError = err instanceof ApiClientError ? err.message : 'Save failed.';
    } finally {
      editSaving = false;
    }
  }

  const batchId = $derived(page.params.id ?? '');
  const locId = $derived(effectiveLocationId());

  $effect(() => {
    if (!locId || !batchId) return;
    let cancelled = false;
    loading = true;
    error = null;
    getCardBatch(locId, batchId)
      .then(async (b) => {
        if (cancelled) return;
        batch = b;
        // Hydrate route names. The batch only stores IDs, so fetch the
        // route list for the location and pick the ones we need.
        // Capped at 500 — if a batch references >500 routes we silently
        // miss names for the overflow (rare; cards are capped much lower).
        try {
          const rs = await listRoutes(locId, { status: 'active', limit: 500 });
          if (cancelled) return;
          const m = new Map<string, RouteShape>();
          for (const r of rs.routes) m.set(r.id, r);
          routes = m;
        } catch {
          // best-effort; we'll show UUIDs if this fails
        }
      })
      .catch((err) => {
        if (cancelled) return;
        error = err instanceof ApiClientError ? err.message : 'Could not load batch.';
      })
      .finally(() => {
        if (!cancelled) loading = false;
      });
    return () => {
      cancelled = true;
    };
  });

  async function handleRetry() {
    if (!locId || !batchId || retrying) return;
    retrying = true;
    retryError = null;
    try {
      const updated = await retryCardBatch(locId, batchId);
      batch = updated;
      previewBroken = false;
    } catch (err) {
      retryError = err instanceof ApiClientError ? err.message : 'Retry failed.';
    } finally {
      retrying = false;
    }
  }

  function cutterLabel(value: string): string {
    return CUTTER_PROFILES.find((p) => p.value === value)?.label ?? value;
  }

  async function handleDelete() {
    if (!locId || !batchId) return;
    if (!confirm('Delete this card batch? The PDF can be regenerated by creating a new batch.')) return;
    deleting = true;
    try {
      await deleteCardBatch(locId, batchId);
      goto('/card-batches');
    } catch (err) {
      error = err instanceof ApiClientError ? err.message : 'Could not delete.';
      deleting = false;
    }
  }

  function fmtDate(iso: string): string {
    return new Date(iso).toLocaleString();
  }

  function themeLabel(t: string): string {
    return t.replace(/_/g, ' ');
  }

  // Count batch route IDs that didn't resolve in the active-routes lookup.
  // Mirrors the HTMX "X of Y routes are no longer available" alert.
  // Routes can be missing for a few reasons: deleted from the DB, moved
  // to another location, or archived (the active-only filter on listRoutes
  // hides archived rows). Either way the PDF won't render a card for them,
  // so this is the right count to surface.
  const resolvedCount = $derived(
    batch ? batch.route_ids.filter((id) => routes.has(id)).length : 0,
  );
  const missingCount = $derived(batch ? batch.route_ids.length - resolvedCount : 0);
</script>

<svelte:head>
  <title>Card batch — Routewerk</title>
</svelte:head>

<div class="page">
  <a class="back" href="/card-batches">← Card batches</a>

  {#if loading}
    <p class="muted">Loading…</p>
